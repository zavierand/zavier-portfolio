function DOM()
{
    // display cli prompt on page
    let cliPrompt = document.createElement("p");
    cliPrompt.setAttribute("")

    // textbox for user input
    var commandInput = document.createElement("input");
    commandInput.setAttribute("");
}

function displayText(arr)
{
    let items = "";
    for(let i = 0; i < arr.length - 1; i++)
    {
        document.write(arr[i]);
    }
    return items;
}

